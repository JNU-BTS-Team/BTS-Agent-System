-- ============================================================================
-- 【创建数据库用户】
-- ============================================================================

-- 创建应用程序连接账户（Flask 后端使用）
CREATE USER IF NOT EXISTS 'bts_app'@'localhost' IDENTIFIED BY 'bts_app_2026';

-- 创建只读账户（对应 viewer 角色）
CREATE USER IF NOT EXISTS 'bts_viewer'@'localhost' IDENTIFIED BY 'viewer_2026';

-- ============================================================================
-- 【分配权限 - 应用账户（全部权限）】
-- ============================================================================
GRANT ALL PRIVILEGES ON SECD.* TO 'bts_app'@'localhost';
GRANT ALL PRIVILEGES ON SECD2.* TO 'bts_app'@'localhost';

-- ============================================================================
-- 【分配权限 - 只读账户（仅查询）】
-- ============================================================================
GRANT SELECT ON SECD.* TO 'bts_viewer'@'localhost';

